package leetcode

// 二叉搜索树的数量
//
// 示例: 1, 2, 3, 4, 5, 6, 7
//
// G(n)表示: n 个元素时, 二叉搜索树的数量;
// F(i,n)表示: 以 i 为根, 二叉搜索树的数量;
//
// 动态规划:
// 1) 问题可以被拆解为规模更小的子问题;
//
// 结果: 与元素数量有关, 与元素的具体值无关;
//

func numTrees(n int) int {
	G := make([]int, n+1)

	G[0] = 1
	G[1] = 1

	for i := 2; i < n+1; i++ {
		for j := 1; j <= i; j++ {
			G[i] += G[j-1] * G[i-j]
		}
	}

	return G[n]
}
